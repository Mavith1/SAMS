    private void btnEnrollActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnrollActionPerformed
       
      String sName = cmbStudent.getSelectedItem().toString();
    String cName = cmbCourse.getSelectedItem().toString();

    // 1. DAO හරහා ID එක ලබා ගැනීම
    int sId = new com.sams.dao.StudentDAO().getStudentIdByName(sName);
    int cId = new com.sams.dao.CourseDAO().getCourseIdByName(cName);

    // ID එක 0 නම් Database එකේ නම නැත
    if(sId == 0 || cId == 0) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: Student or Course not found in database!");
        return;
    }

    // 2. Enrollment Object එකක් හදන්න
    com.sams.model.Enrollment enrollment = new com.sams.model.Enrollment();
    enrollment.setStudentId(sId);
    enrollment.setCourseId(cId);
    enrollment.setEnrollmentDate(new java.text.SimpleDateFormat("yyyy-MM-dd").format(new java.util.Date()));

    // 3. Service එක හරහා Save කරන්න
    com.sams.service.EnrollmentService service = new com.sams.service.EnrollmentService();
    boolean success = service.enrollStudent(enrollment); 

    if (success) {
        javax.swing.JOptionPane.showMessageDialog(this, "Student Enrolled Successfully!");
        loadEnrollments(); // Table එක Refresh කරන්න
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Error in Enrolling! (Check Database)");
    }  
        
    }//GEN-LAST:event_btnEnrollActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = tblEnrollment.getSelectedRow();

    // 2. කිසිවක් තෝරා නැත්නම් Error පණිවිඩයක් දීම
    if (selectedRow == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "කරුණාකර ඉවත් කිරීමට අවශ්‍ය පේළියක් Table එකෙන් තෝරන්න!");
        return;
    }

  
    int id = Integer.parseInt(tblEnrollment.getValueAt(selectedRow, 0).toString());

    
    int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "ඔබට මෙම Enrollment වාර්තාව මකාදැමීමට අවශ්‍යද?", "Confirm Delete", javax.swing.JOptionPane.YES_NO_OPTION);
    
    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
   
        com.sams.dao.EnrollmentDAO dao = new com.sams.dao.EnrollmentDAO();
        if (dao.deleteEnrollment(id)) {
            javax.swing.JOptionPane.showMessageDialog(this, "සාර්ථකව මකාදමන ලදී!");
            // 6. Table එක නැවත Refresh කිරීම
            loadEnrollments(); 
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "මකාදැමීමේ දෝෂයක් සිදුවිය!");
        }
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

