    private void initComboBoxes() {
        
        // 1. ශිෂ්‍යයන් Load කිරීම
        com.sams.dao.StudentDAO sDao = new com.sams.dao.StudentDAO();
        java.util.List<com.sams.model.Student> students = sDao.getAllStudents();
        
        cmbStudent.removeAllItems(); // පරණ ඒව අයින් කරන්න
        for (com.sams.model.Student s : students) {
            // මෙතනදී පේන්නේ නම විතරයි, නමුත් object එකක් විදිහට තමයි තියෙන්නේ
            cmbStudent.addItem(s.getStudentName()); 
        }

        // 2. පාඨමාලා Load කිරීම
        com.sams.dao.CourseDAO cDao = new com.sams.dao.CourseDAO();
        java.util.List<com.sams.model.Course> courses = cDao.getAllCourses();
        
        cmbCourse.removeAllItems(); // පරණ ඒව අයින් කරන්න
        for (com.sams.model.Course c : courses) {
            cmbCourse.addItem(c.getCourseName());
        }
    }
    private void loadEnrollments() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblEnrollment.getModel();
        model.setRowCount(0); 

        com.sams.dao.EnrollmentDAO dao = new com.sams.dao.EnrollmentDAO();
        java.util.List<com.sams.model.Enrollment> list = dao.getAllEnrollments();

        for (com.sams.model.Enrollment e : list) {
            model.addRow(new Object[]{e.getId(), e.getStudentName(), e.getCourseName(), e.getEnrollmentDate()});
        }
    }
    
